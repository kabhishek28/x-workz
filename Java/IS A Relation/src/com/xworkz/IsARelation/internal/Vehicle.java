package com.xworkz.IsARelation.internal;

public class Vehicle {
        public Vehicle() {
            System.out.println("No-argument constructor: Vehicle");
        }

        public void fuelType() {
            System.out.println("Fuel type: Petrol/Diesel/Electric");
        }

        public void speed() {
            System.out.println("Speed: 100 km/h");
        }

        public void capacity() {
            System.out.println("Capacity: 5 persons");
        }

        public void wheels() {
            System.out.println("Wheels: 4");
        }

        public void engine() {
            System.out.println("Engine: 1200cc");
        }
}
