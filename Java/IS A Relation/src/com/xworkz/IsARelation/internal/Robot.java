package com.xworkz.IsARelation.internal;

public class Robot {
    public Robot() {
        System.out.println("No-argument constructor: Robot");
    }

    public void powerOn() {
        System.out.println("Robot is powered on");
    }

    public void move() {
        System.out.println("Robot is moving");
    }

    public void processData() {
        System.out.println("Robot is processing data");
    }

    public void communicate() {
        System.out.println("Robot is communicating");
    }

    public void shutdown() {
        System.out.println("Robot is shutting down");
    }
}
