package com.xworkz.IsARelation.internal;

public class Tree {
    public Tree() {
        System.out.println("No-argument constructor: Tree");
    }

    public void grow() {
        System.out.println("Tree is growing...");
    }

    public void photosynthesize() {
        System.out.println("Tree is performing photosynthesis...");
    }

    public void absorbWater() {
        System.out.println("Tree is absorbing water...");
    }

    public void provideOxygen() {
        System.out.println("Tree is providing oxygen...");
    }

    public void shedLeaves() {
        System.out.println("Tree is shedding leaves...");
    }
}
