package com.xworkz.IsARelation.internal;

public class River {
    public River() {
        System.out.println("River Constructor");
    }

    public void flow() {
        System.out.println("River is flowing");
    }

    public void erodeLand() {
        System.out.println("River is eroding land");
    }

    public void supportLife() {
        System.out.println("River supports aquatic life");
    }

    public void carrySediments() {
        System.out.println("River carries sediments");
    }

    public void provideWater() {
        System.out.println("River provides water for irrigation and drinking");
    }
}
