package com.xworkz.IsARelation.internal;

public class Train {
    public Train() {
        System.out.println("Train Constructor");
    }

    public void startJourney() {
        System.out.println("Train is starting its journey");
    }

    public void stopAtStation() {
        System.out.println("Train is stopping at a station");
    }

    public void increaseSpeed() {
        System.out.println("Train is increasing speed");
    }

    public void blowHorn() {
        System.out.println("Train is blowing its horn");
    }

    public void providePassengerService() {
        System.out.println("Train is providing service to passengers");
    }
}
