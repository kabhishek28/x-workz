package com.xworkz.IsARelation.internal;

public class Reptile {
    public Reptile() {
        System.out.println("No-argument constructor: Reptile");
    }

    public void crawl() {
        System.out.println("Reptile is crawling...");
    }

    public void layEggs() {
        System.out.println("Reptile is laying eggs...");
    }

    public void coldBlooded() {
        System.out.println("Reptile is cold-blooded...");
    }

    public void shedSkin() {
        System.out.println("Reptile is shedding its skin...");
    }

    public void camouflage() {
        System.out.println("Reptile is camouflaging...");
    }
}
