package com.xworkz.IsARelation.internal;

public class Submarine {
    public Submarine() {
        System.out.println("No-argument constructor: Submarine");
    }

    public void dive() {
        System.out.println("Submarine is diving underwater");
    }

    public void surface() {
        System.out.println("Submarine is surfacing");
    }

    public void navigate() {
        System.out.println("Submarine is navigating");
    }

    public void sonarScan() {
        System.out.println("Submarine is scanning using sonar");
    }

    public void fireTorpedo() {
        System.out.println("Submarine is firing a torpedo");
    }
}
