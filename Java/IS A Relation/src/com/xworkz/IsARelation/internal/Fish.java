package com.xworkz.IsARelation.internal;

public class Fish {
    public Fish() {
        System.out.println("No-argument constructor: Fish");
    }

    public void swim() {
        System.out.println("Fish is swimming...");
    }

    public void breatheUnderwater() {
        System.out.println("Fish is breathing underwater...");
    }

    public void layEggs() {
        System.out.println("Fish is laying eggs...");
    }

    public void detectVibrations() {
        System.out.println("Fish is detecting vibrations in the water...");
    }

    public void changeDepth() {
        System.out.println("Fish is adjusting its depth...");
    }
}
