package com.xworkz.IsARelation.internal;

public class Gadget {

    public Gadget() {
        System.out.println("Gadget is activated...");
    }

    public void powerOn() {
        System.out.println("Powering on the gadget...");
    }

    public void powerOff() {
        System.out.println("Powering off the gadget...");
    }

    public void performFunction() {
        System.out.println("Gadget is performing its function...");
    }

    public void connectToOtherDevice() {
        System.out.println("Connecting gadget to another device...");
    }

    public void displayInfo() {
        System.out.println("Displaying gadget information...");
    }
}
