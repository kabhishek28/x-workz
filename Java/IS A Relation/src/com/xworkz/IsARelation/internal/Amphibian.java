package com.xworkz.IsARelation.internal;

public class Amphibian {
    public Amphibian() {
        System.out.println("No-argument constructor: Amphibian");
    }

    public void swim() {
        System.out.println("Amphibian is swimming...");
    }

    public void jump() {
        System.out.println("Amphibian is jumping...");
    }

    public void breatheThroughSkin() {
        System.out.println("Amphibian is breathing through its skin...");
    }

    public void layEggsInWater() {
        System.out.println("Amphibian is laying eggs in water...");
    }

    public void transitionToLand() {
        System.out.println("Amphibian is transitioning from water to land...");
    }
}
