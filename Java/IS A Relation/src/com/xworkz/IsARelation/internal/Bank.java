package com.xworkz.IsARelation.internal;

public class Bank {
    public Bank() {
        System.out.println("No-argument constructor: Bank");
    }

    public void deposit() {
        System.out.println("Deposit money in bank");
    }

    public void withdraw() {
        System.out.println("Withdraw money from bank");
    }

    public void loan() {
        System.out.println("Bank provides loans");
    }

    public void interestRate() {
        System.out.println("Bank has a standard interest rate");
    }

    public void branchInfo() {
        System.out.println("Bank has multiple branches");
    }
}
