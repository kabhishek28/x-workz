class Long_data_tp {
    public static void main(String[] args) {
        // Long Type
        long distance = 9876543210L;
        long population = 7800000000L;
        long revenue = 25000000000L;
        long storage = 500000000000L;
        long transactions = 999999999L;

        System.out.println("First initialization values (long):");
        System.out.println(distance);
        System.out.println(population);
        System.out.println(revenue);
        System.out.println(storage);
        System.out.println(transactions);

        long length = distance;
        long users = population;
        long profit = revenue;
        long capacity = storage;
        long records = transactions;

        System.out.println("Re-initialization values (long):");
        System.out.println(length);
        System.out.println(users);
        System.out.println(profit);
        System.out.println(capacity);
        System.out.println(records);
	}
}