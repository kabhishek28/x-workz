class Int_data_tp{
	public static void main(String[] args){
		int tier = 100;
		int prestige = 15;
		int fame = 10;
		int honor = 104;
		int badge = 100;
		System.out.println("first initialization values");
		System.out.println(tier);
		System.out.println(prestige);
		System.out.println(fame);
		System.out.println(honor);
		System.out.println(badge);
		int rating = tier;
		int title = prestige;
		int victories = fame;
		int achievements = honor;
		int specific = badge;
		System.out.println("re_initialization values");
		System.out.println(rating);
		System.out.println(title);
		System.out.println(victories);
		System.out.println(achievements);
		System.out.println(specific);
	}
}
		
		
		